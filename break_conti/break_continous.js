// break and continous use in loop  and switch

for (var i=0; i<10; i++){
if (i==5){
    break // break
} 
console.log(i)  
}

for (var j=0; j<10; j++){
    if (j==5){
        continue // continue
    }
    console.log(j)
}

// Note: break  stop loop when condition satisfy
// Note: continue  ingore certain value when satisfy 
